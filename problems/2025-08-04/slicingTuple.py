data = (5, 10, 15, 20, 25, 30, 35, 40)

#Extract the middle three elements
print(data[2:5])

#Show the last 4 elements in reverse order
print(data[-1:-5:-1])

#Show values at even indexes
print(data[0:8:2])

#Reverse the entire tuple
print(data[-1: :-1])

#Show all except the first and last elements
print(data[1:7:])